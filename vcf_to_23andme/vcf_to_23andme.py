def vcf_needs_annotation(vcf_path: str, sample_lines: int = 500) -> bool:
    """
    Check whether a VCF needs rsID annotation by sampling variant lines.
    Returns True if >80% of sampled variants have '.' in the ID column.
    """
    total = 0
    missing = 0
    with open_file(vcf_path) as f:
        for line in f:
            if line.startswith("#"):
                continue
            fields = line.split("\t", 4)
            if len(fields) < 4:
                continue
            total += 1
            if fields[2] == ".":
                missing += 1
            if total >= sample_lines:
                break

    if total == 0:
        return False
    ratio = missing / total
    return ratio > 0.8


def detect_vcf_chrom_style(vcf_path: str) -> str:
    """
    Detect chromosome naming style in a VCF file.
    Returns "chr" (chr1, chr2...), "nochr" (1, 2...), or "refseq" (NC_000001.11...).
    """
    with open_file(vcf_path) as f:
        for line in f:
            if line.startswith("#"):
                if line.startswith("##contig="):
                    m = re.search(r"ID=(\S+?)[,>]", line)
                    if m:
                        cid = m.group(1)
                        if cid.startswith("NC_"):
                            return "refseq"
                        if cid.startswith("chr"):
                            return "chr"
                        if cid in ("1", "2", "3", "X", "Y"):
                            return "nochr"
                continue
            fields = line.split("\t", 2)
            if fields:
                chrom = fields[0]
                if chrom.startswith("NC_"):
                    return "refseq"
                if chrom.startswith("chr"):
                    return "chr"
                return "nochr"
    return "nochr"


def download_file(url: str, dest: Path) -> None:
    """Download a file using wget (with resume support) or curl as fallback."""
    dest.parent.mkdir(parents=True, exist_ok=True)

    if dest.exists():
        print(f"  Already downloaded: {dest.name}")
        return

    partial = dest.with_suffix(dest.suffix + ".partial")
    print(f"  Downloading {dest.name}...")
    print(f"    URL: {url}")

    try:
        subprocess.run(
            ["wget", "-q", "--show-progress", "-c", "-O", str(partial), url],
            check=True,
        )
    except FileNotFoundError:
        subprocess.run(
            ["curl", "-L", "-C", "-", "-o", str(partial), url],
            check=True,
        )

    partial.rename(dest)
    print(f"  Download complete: {dest.name}")


def _build_chrom_map_file(source_style: str, target_style: str, ref_dir: Path) -> Path | None:
    """
    Create a chromosome name mapping file for bcftools annotate --rename-chrs.
    Maps chromosome names in the dbSNP file to match the input VCF style.
    Returns the path to the mapping file, or None if no mapping is needed.
    """
    if source_style == target_style:
        return None

    standard_chroms = [str(i) for i in range(1, 23)] + ["X", "Y", "MT"]

    mapping = {}
    if source_style == "refseq":
        chrom_to_refseq = {v: k for k, v in REFSEQ_TO_CHROM.items()}
        for c in standard_chroms:
            refseq = chrom_to_refseq.get(c)
            if refseq:
                if target_style == "chr":
                    target = f"chr{c}" if c != "MT" else "chrM"
                else:
                    target = c
                mapping[refseq] = target
    elif source_style == "nochr" and target_style == "chr":
        for c in standard_chroms:
            mapping[c] = f"chr{c}" if c != "MT" else "chrM"
    elif source_style == "chr" and target_style == "nochr":
        for c in standard_chroms:
            src = f"chr{c}" if c != "MT" else "chrM"
            mapping[src] = c
    else:
        return None

    if not mapping:
        return None

    map_path = ref_dir / f"chrom_map_{source_style}_to_{target_style}.txt"
    if not map_path.exists():
        with open(map_path, "w") as f:
            for src, dst in mapping.items():
                f.write(f"{src} {dst}\n")
    return map_path


def ensure_dbsnp_available(build: str, ref_dir: Path, vcf_chrom_style: str) -> Path:
    """
    Ensure a dbSNP VCF file with matching chromosome names is available in ref_dir.
    Downloads if necessary and renames chromosomes if styles differ.
    Returns the path to the ready-to-use dbSNP VCF.
    """
    ref_dir = Path(ref_dir)
    ref_dir.mkdir(parents=True, exist_ok=True)

    info = DBSNP_URLS[build]
    raw_vcf = ref_dir / info["filename"]
    raw_tbi = ref_dir / (info["filename"] + ".tbi")

    download_file(info["vcf"], raw_vcf)
    download_file(info["tbi"], raw_tbi)

    dbsnp_style = detect_vcf_chrom_style(str(raw_vcf))
    print(f"  dbSNP chromosome style: {dbsnp_style}, input VCF style: {vcf_chrom_style}")

    if dbsnp_style == vcf_chrom_style:
        return raw_vcf

    renamed_name = raw_vcf.stem.replace(".vcf", "") + f".{vcf_chrom_style}.vcf.gz"
    renamed_vcf = ref_dir / renamed_name

    if renamed_vcf.exists() and Path(str(renamed_vcf) + ".tbi").exists():
        print(f"  Using pre-existing renamed dbSNP: {renamed_vcf.name}")
        return renamed_vcf

    chrom_map = _build_chrom_map_file(dbsnp_style, vcf_chrom_style, ref_dir)
    if chrom_map is None:
        print(f"  Warning: could not build chromosome name mapping, using dbSNP as-is")
        return raw_vcf

    print(f"  Renaming dbSNP chromosomes ({dbsnp_style} -> {vcf_chrom_style})...")
    run_cmd(
        ["bcftools", "annotate", "--rename-chrs", str(chrom_map),
         str(raw_vcf), "-Oz", "-o", str(renamed_vcf)],
        "bcftools annotate --rename-chrs",
    )
    run_cmd(
        ["bcftools", "index", "-t", str(renamed_vcf)],
        "bcftools index",
    )
    print(f"  Renamed dbSNP saved: {renamed_vcf.name}")
    return renamed_vcf


def _ensure_vcf_indexed(vcf_path: str) -> str:
    """
    Ensure a VCF file is bgzipped and tabix-indexed for bcftools.
    The original input file is NEVER modified.

    If the .tbi index already exists, returns the path unchanged.
    Otherwise creates a bgzipped copy with a .bgz.vcf.gz suffix and indexes it.
    Returns the path to the indexed VCF (may differ from input).
    """
    vcf = Path(vcf_path)
    tbi = Path(f"{vcf}.tbi")
    csi = Path(f"{vcf}.csi")

    if tbi.exists() or csi.exists():
        return vcf_path

    if str(vcf).endswith(".vcf.gz"):
        result = subprocess.run(
            ["bcftools", "index", "-t", str(vcf)],
            capture_output=True, text=True,
        )
        if result.returncode == 0:
            return vcf_path

    stem = vcf.name
    for ext in (".vcf.gz", ".vcf"):
        if stem.endswith(ext):
            stem = stem[: -len(ext)]
            break
    bgz_vcf = vcf.parent / f"{stem}.bgz.vcf.gz"

    if bgz_vcf.exists() and Path(f"{bgz_vcf}.tbi").exists():
        print(f"  Using existing bgzipped copy: {bgz_vcf.name}")
        return str(bgz_vcf)

    print(f"  Creating bgzipped copy for indexing (original file is not modified)...")
    run_cmd(
        ["bcftools", "view", str(vcf), "-Oz", "-o", str(bgz_vcf)],
        "bcftools view (bgzip copy)",
    )
    run_cmd(
        ["bcftools", "index", "-t", str(bgz_vcf)],
        "bcftools index",
    )
    print(f"  Indexed copy created: {bgz_vcf.name}")
    return str(bgz_vcf)


def annotate_vcf_with_dbsnp(vcf_path: str, build: str, ref_dir: str) -> str:
    """
    Annotate a VCF file with dbSNP rsIDs if most variants lack them.

    Steps:
      1. Check if annotation is needed (sample variant lines)
      2. Download dbSNP to ref_dir if not already present
      3. Handle chromosome name mismatches
      4. Run bcftools annotate -c ID
      5. Save annotated VCF alongside the original with .rsid.vcf.gz suffix

    Returns the path to the annotated VCF (or the original if annotation was skipped).
    """
    vcf = Path(vcf_path)
    ref = Path(ref_dir)

    stem = vcf.name
    for ext in (".vcf.gz", ".vcf"):
        if stem.endswith(ext):
            stem = stem[: -len(ext)]
            break
    annotated_vcf = vcf.parent / f"{stem}.rsid.vcf.gz"

    if annotated_vcf.exists():
        print(f"  Annotated VCF already exists: {annotated_vcf.name}")
        print(f"  Skipping re-annotation. Delete the file to force re-annotation.")
        return str(annotated_vcf)

    print(f"Checking whether VCF needs rsID annotation...")
    if not vcf_needs_annotation(vcf_path):
        print(f"  VCF already has rsIDs, skipping annotation.")
        return vcf_path

    print(f"  Most variants lack rsIDs. Will annotate with dbSNP.")

    vcf_style = detect_vcf_chrom_style(vcf_path)
    dbsnp_vcf = ensure_dbsnp_available(build, ref, vcf_style)

    indexed_vcf = _ensure_vcf_indexed(vcf_path)

    print(f"Running bcftools annotate...")
    run_cmd(
        ["bcftools", "annotate", "-a", str(dbsnp_vcf), "-c", "ID",
         str(indexed_vcf), "-Oz", "-o", str(annotated_vcf)],
        "bcftools annotate -c ID",
    )
    run_cmd(
        ["bcftools", "index", "-t", str(annotated_vcf)],
        "bcftools index",
    )
    print(f"  Annotated VCF saved: {annotated_vcf}")
    return str(annotated_vcf)


