def vcf_genotype_to_23andme(gt_str: str, ref: str, alt: str, chrom: str, is_male: bool = False) -> str:
    """
    Convert a VCF genotype to 23andMe format.

    Autosomes and female X: two letters (e.g. AA, AG).
    Male X, Y, MT: single letter (e.g. A, G).
    Indels: II/DD/DI.
    No-call: --.
    """
    if gt_str in ("./.", ".|.", "."):
        return "--"

    sep = "|" if "|" in gt_str else "/"
    allele_indices = gt_str.split(sep)

    if len(allele_indices) < 2:
        return "--"

    all_alleles = [ref] + (alt.split(",") if alt and alt != "." else [])

    bases = []
    for idx_str in allele_indices:
        if idx_str == ".":
            return "--"
        try:
            idx = int(idx_str)
        except ValueError:
            return "--"
        if idx >= len(all_alleles):
            return "--"
        bases.append(all_alleles[idx])

    is_haploid = chrom in ("Y", "MT") or (chrom == "X" and is_male)

    is_indel = any(len(b) > 1 for b in bases) or any(len(b) > 1 for b in all_alleles if b != ".")

    if is_indel:
        if bases[0] == bases[1]:
            if bases[0] == ref:
                return "D" if is_haploid else "DD"
            return "I" if is_haploid else "II"
        return "DI"

    if is_haploid:
        return bases[0]

    return bases[0] + bases[1]


def generate_header(build: str, version: str) -> str:
    """Generate the 23andMe file header (V3 or V5)."""
    build_labels = {
        "GRCh37": "build 37 (also known as Annotation Release 104)",
        "GRCh38": "build 38 (also known as Annotation Release 106)",
    }
    build_label = build_labels.get(build, f"build {build}")
    date_str = datetime.now().strftime("%a %b %d %H:%M:%S %Y")

    template = HEADER_23ANDME_V5 if version == "V5" else HEADER_23ANDME_V3
    return template.format(date=date_str, build_label=build_label)


