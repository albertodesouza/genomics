def load_config(config_path: Path) -> dict:
    """Load YAML configuration file."""
    with open(config_path, "r", encoding="utf-8") as f:
        config = yaml.safe_load(f)
    return config


def open_file(filename: str):
    """Open a regular or gzipped file automatically."""
    if str(filename).endswith(".gz"):
        return gzip.open(filename, "rt", encoding="utf-8")
    return open(filename, "r", encoding="utf-8")


def normalize_chrom(chrom: str) -> str:
    """Strip 'chr' prefix and normalise MT."""
    c = chrom.removeprefix("chr")
    if c == "M":
        c = "MT"
    return c


def run_cmd(cmd: list[str], description: str = "") -> subprocess.CompletedProcess:
    """Run an external command, raising on failure."""
    desc = description or " ".join(cmd[:3])
    try:
        result = subprocess.run(cmd, check=True, capture_output=True, text=True)
        return result
    except FileNotFoundError:
        print(f"Error: command not found: {cmd[0]}")
        print(f"  Make sure the genomics conda environment is activated.")
        sys.exit(1)
    except subprocess.CalledProcessError as exc:
        print(f"Error running {desc}:")
        print(f"  Command: {' '.join(cmd)}")
        if exc.stderr:
            print(f"  stderr: {exc.stderr.strip()}")
        sys.exit(1)


