def detect_genome_build(vcf_path: str) -> str | None:
    """
    Detect genome build (GRCh37 or GRCh38) from VCF headers.

    Strategy (in priority order):
      1. ##reference= line containing GRCh37/hg19 or GRCh38/hg38
      2. ##contig= lines with known chromosome lengths
      3. Chromosome naming convention (chr prefix suggests GRCh38)

    Returns "GRCh37", "GRCh38", or None.
    """
    grch37_hits = 0
    grch38_hits = 0
    has_chr_prefix = False
    has_no_chr_prefix = False

    with open_file(vcf_path) as f:
        for line in f:
            if not line.startswith("#"):
                break

            if line.startswith("##reference="):
                ref_line = line.lower()
                if "grch37" in ref_line or "hg19" in ref_line or "b37" in ref_line:
                    return "GRCh37"
                if "grch38" in ref_line or "hg38" in ref_line:
                    return "GRCh38"

            if line.startswith("##contig="):
                m = re.search(r"ID=(\S+?)[,>]", line)
                m_len = re.search(r"length=(\d+)", line)
                if m and m_len:
                    contig_id = normalize_chrom(m.group(1))
                    contig_len = int(m_len.group(1))

                    if contig_id in GRCH37_CONTIG_LENGTHS and GRCH37_CONTIG_LENGTHS[contig_id] == contig_len:
                        grch37_hits += 1
                    if contig_id in GRCH38_CONTIG_LENGTHS and GRCH38_CONTIG_LENGTHS[contig_id] == contig_len:
                        grch38_hits += 1

                    raw_id = m.group(1)
                    if raw_id.startswith("chr"):
                        has_chr_prefix = True
                    elif raw_id in ("1", "2", "3", "X", "Y"):
                        has_no_chr_prefix = True

            if line.startswith("#CHROM"):
                break

    if grch37_hits > 0 or grch38_hits > 0:
        if grch37_hits > grch38_hits:
            return "GRCh37"
        if grch38_hits > grch37_hits:
            return "GRCh38"

    if has_chr_prefix and not has_no_chr_prefix:
        return "GRCh38"
    if has_no_chr_prefix and not has_chr_prefix:
        return "GRCh37"

    return None


