def convert_vcf_to_23andme(config: dict) -> None:
    """Main VCF -> 23andMe conversion pipeline."""
    vcf_file = config["input"]["vcf_file"]
    output_file = config["output"]["output_file"]
    format_version = config.get("output", {}).get("format_version", "V5")
    config_build = config.get("reference_build", "GRCh37")
    sample_name = config.get("input", {}).get("sample", None)
    skip_no_rsid = config.get("output", {}).get("skip_no_rsid", False)
    panel_path = config.get("output", {}).get("snp_panel", None)
    filter_by_chip_panel = config.get("output", {}).get("filter_by_chip_panel", False)

    annotation_cfg = config.get("annotation", {})
    annotation_enabled = annotation_cfg.get("enabled", True)
    ref_dir = annotation_cfg.get("ref_dir", None)

    if not Path(vcf_file).exists():
        print(f"Error: VCF file not found: {vcf_file}")
        sys.exit(1)

    if format_version not in ("V3", "V5"):
        print(f"Error: format_version must be 'V3' or 'V5', got: '{format_version}'")
        sys.exit(1)

    # --- Step 1: Detect genome build ---
    print(f"Detecting genome build from VCF...")
    detected_build = detect_genome_build(vcf_file)
    if detected_build:
        build = detected_build
        print(f"  Detected build: {build}")
    else:
        build = config_build
        print(f"  Could not detect build automatically.")
        print(f"  Using YAML fallback: {build}")

    # --- Step 2: Annotate with dbSNP if needed ---
    active_vcf = vcf_file
    if annotation_enabled:
        if ref_dir is None:
            print(f"Error: annotation.enabled is true but annotation.ref_dir is not set.")
            print(f"  Please set annotation.ref_dir in the YAML config.")
            sys.exit(1)
        active_vcf = annotate_vcf_with_dbsnp(vcf_file, build, ref_dir)

    # --- Step 3: Load SNP panel ---
    panel: set[str] | None = None
    if panel_path:
        if not Path(panel_path).exists():
            print(f"Error: SNP panel file not found: {panel_path}")
            sys.exit(1)
        panel = load_snp_panel(panel_path)
        print(f"Loaded SNP panel: {len(panel):,} rsIDs from {panel_path}")
    elif filter_by_chip_panel:
        if ref_dir is None:
            print(f"Error: filter_by_chip_panel is true but annotation.ref_dir is not set.")
            print(f"  Please set annotation.ref_dir in the YAML config.")
            sys.exit(1)
        chip_panel_path = ensure_chip_panel_available(format_version, ref_dir)
        panel = load_snp_panel(str(chip_panel_path))
        print(f"Loaded chip panel ({format_version}): {len(panel):,} rsIDs")

    # --- Step 4: Read VCF and convert ---
    print(f"Reading VCF: {active_vcf}")
    print(f"Output format: 23andMe {format_version}")
    print(f"Build: {build}")

    sample_idx = None
    sample_id = None
    variants = []
    line_count = 0
    panel_hits = 0

    with open_file(active_vcf) as f:
        for line in f:
            if line.startswith("##"):
                continue

            if line.startswith("#CHROM"):
                fields = line.strip().split("\t")
                samples = fields[9:]

                if not samples:
                    print("Error: no samples found in VCF.")
                    sys.exit(1)

                if sample_name:
                    if sample_name in samples:
                        sample_idx = samples.index(sample_name)
                        sample_id = sample_name
                    else:
                        print(f"Error: sample '{sample_name}' not found in VCF.")
                        print(f"  Available samples: {', '.join(samples)}")
                        sys.exit(1)
                else:
                    sample_idx = 0
                    sample_id = samples[0]

                if len(samples) > 1:
                    print(f"  VCF contains {len(samples)} samples. Using: {sample_id}")
                else:
                    print(f"  Sample: {sample_id}")
                continue

            if sample_idx is None:
                continue

            fields = line.strip().split("\t")
            if len(fields) < 10:
                continue

            line_count += 1
            if line_count % 500000 == 0:
                print(f"  Reading VCF: {line_count:,} lines scanned...", end="\r")

            chrom_raw, pos, rsid, ref, alt = fields[0], fields[1], fields[2], fields[3], fields[4]
            chrom = normalize_chrom(chrom_raw)

            if rsid == ".":
                if skip_no_rsid:
                    continue
                rsid = f"i{chrom}_{pos}"

            if panel is not None:
                if rsid in panel:
                    panel_hits += 1
                else:
                    continue

            fmt_fields = fields[8].split(":")
            try:
                gt_index = fmt_fields.index("GT")
            except ValueError:
                continue

            sample_data = fields[9 + sample_idx].split(":")
            if gt_index >= len(sample_data):
                continue

            gt = sample_data[gt_index]
            genotype = vcf_genotype_to_23andme(gt, ref, alt, chrom)

            variants.append((rsid, chrom, pos, genotype))

    # --- Step 5: Report statistics ---
    rsid_count = sum(1 for v in variants if v[0].startswith("rs"))
    internal_count = len(variants) - rsid_count
    print(f"\n  VCF lines scanned: {line_count:,}")
    print(f"  Variants written to output: {len(variants):,}")
    if rsid_count:
        print(f"    With rsID (from dbSNP): {rsid_count:,}")
    if internal_count:
        print(f"    With positional ID (no rsID available): {internal_count:,}")
    if panel is not None:
        print(f"  Chip panel filtering: {panel_hits:,} of {len(panel):,} panel SNPs found in VCF ({100*panel_hits/len(panel):.1f}%)")

    # --- Step 6: Write output ---
    out_path = Path(output_file)
    out_path.parent.mkdir(parents=True, exist_ok=True)

    header = generate_header(build, format_version)

    with open(out_path, "w", encoding="utf-8") as out:
        out.write(header)
        for rsid, chrom, pos, genotype in variants:
            out.write(f"{rsid}\t{chrom}\t{pos}\t{genotype}\n")

    print(f"Output file written: {output_file}")
    print(f"Conversion complete.")


