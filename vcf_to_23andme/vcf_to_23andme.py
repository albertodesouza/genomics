def load_snp_panel(panel_path: str) -> set[str]:
    """Load a SNP panel file (one rsID per line) into a set."""
    panel = set()
    with open(panel_path, "r", encoding="utf-8") as f:
        for line in f:
            rsid = line.strip()
            if rsid and not rsid.startswith("#"):
                panel.add(rsid)
    return panel


def _extract_rsids_from_illumina_csv(csv_path: Path) -> list[str]:
    """
    Parse an Illumina manifest CSV and extract rsIDs from the Name column.

    Illumina manifests have sections [Header], [Assay], [Controls].
    The probe data is in [Assay]; the Name column contains rsIDs for most probes.
    """
    rsids = []
    in_assay = False
    name_idx = None

    with open(csv_path, "r", encoding="utf-8", errors="replace") as f:
        for line in f:
            stripped = line.strip()

            if stripped == "[Assay]":
                in_assay = True
                continue

            if stripped.startswith("[") and stripped.endswith("]") and in_assay:
                break

            if not in_assay:
                continue

            fields = stripped.split(",")

            if name_idx is None:
                try:
                    name_idx = fields.index("Name")
                except ValueError:
                    for i, col in enumerate(fields):
                        if col.strip().lower() == "name":
                            name_idx = i
                            break
                    if name_idx is None:
                        print("  Warning: could not find 'Name' column in manifest")
                        return rsids
                continue

            if len(fields) > name_idx:
                name = fields[name_idx].strip()
                if name.startswith("rs"):
                    rsids.append(name)

    return rsids


def ensure_chip_panel_available(format_version: str, ref_dir: str) -> Path:
    """
    Ensure a 23andMe chip SNP panel file is available in ref_dir.

    Downloads the Illumina manifest for the chip matching format_version,
    extracts rsIDs, and caches the result as a simple text file.
    Returns the path to the panel file.
    """
    ref = Path(ref_dir)
    ref.mkdir(parents=True, exist_ok=True)

    info = ILLUMINA_PANEL_URLS[format_version]
    panel_file = ref / info["panel_file"]

    if panel_file.exists():
        print(f"  Chip panel already available: {panel_file.name}")
        return panel_file

    print(f"  Downloading {info['chip_name']} manifest to extract SNP panel...")
    download_dest = ref / info["download_name"]
    download_file(info["url"], download_dest)

    if info["is_zip"]:
        print(f"  Extracting CSV from ZIP archive...")
        with zipfile.ZipFile(download_dest, "r") as zf:
            csv_names = [n for n in zf.namelist() if n.lower().endswith(".csv")]
            if not csv_names:
                print(f"  Error: no CSV file found inside {download_dest.name}")
                sys.exit(1)
            csv_name = csv_names[0]
            zf.extract(csv_name, ref)
            csv_path = ref / csv_name
    else:
        csv_path = download_dest

    print(f"  Parsing manifest to extract rsIDs...")
    rsids = _extract_rsids_from_illumina_csv(csv_path)
    print(f"  Found {len(rsids):,} rsIDs in manifest")

    with open(panel_file, "w", encoding="utf-8") as f:
        f.write(f"# 23andMe {format_version} chip panel - {info['chip_name']}\n")
        f.write(f"# Extracted from Illumina manifest ({len(rsids)} rsIDs)\n")
        for rsid in sorted(set(rsids)):
            f.write(f"{rsid}\n")

    panel_count = len(set(rsids))
    print(f"  Panel saved: {panel_file.name} ({panel_count:,} unique rsIDs)")

    if csv_path != download_dest and csv_path.exists():
        csv_path.unlink()
    if download_dest.exists():
        download_dest.unlink()

    return panel_file


